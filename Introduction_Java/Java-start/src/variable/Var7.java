package variable;

public class Var7 {
    public static void main(String[] args) {
        int a = 100;    // 정수를 담을 수 있음
        double b = 10.5;    // 실수를 담을 수 있음
        boolean c = true;   // true와 false만 담을 수 있음
        char d = 'A';   // 문자 하나를 담을 수 있음
        String e = "Hello Java";    //문자열을 담을 수 있음

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
