package variable;

public class Var8 {

    public static void main(String[] args) {
        // 정수
        byte b = 127;   // -128~127까지
        short s = 32767; // -32.768 ~ 32,767까지
        int i = 214748367;   //-2,147,483,648 ~ 2,147,483,647

        // 실수
        float f = 10.0f;
        double d = 10.0;
    }
}

// 정수는 기본형으로 int를, 실수는 기본형으로 double을 사용한다.